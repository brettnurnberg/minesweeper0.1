/*********************************************************************
*
*   Class:
*       menu_item_type
*
*   Description:
*       Represents single item in a menu
*           Should contain the name, size, and callback function
*
*********************************************************************/

/*--------------------------------------------------------------------
                            INCLUDES
--------------------------------------------------------------------*/

using System;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Input;

/*--------------------------------------------------------------------
                           NAMESPACE
--------------------------------------------------------------------*/

namespace minesweeper {

/*--------------------------------------------------------------------
                           DELEGATES
--------------------------------------------------------------------*/

public delegate void menu_item_handler();

/*--------------------------------------------------------------------
                             CLASS
--------------------------------------------------------------------*/

public class menu_item_type {

/*--------------------------------------------------------------------
                           ATTRIBUTES
--------------------------------------------------------------------*/

private menu_item_handler hndlr;
public  string name;
public  Boolean highlight;
/*--------------------------------------------------------------------
                            METHODS
--------------------------------------------------------------------*/

/***********************************************************
*
*   Method:
*       menu_item_type
*
*   Description:
*       Constructor.
*
***********************************************************/

public menu_item_type( string item_name, menu_item_handler item_hndlr )
{
/*----------------------------------------------------------
Initialize
----------------------------------------------------------*/
name = item_name;
hndlr = item_hndlr;
highlight = false;

} /* menu_item_type() */


/***********************************************************
*
*   Method:
*       handler
*
*   Description:
*       Constructor.
*
***********************************************************/

public void handler()
{
hndlr();
} /* menu_item_type() */

}
}
